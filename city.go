package ircity

// City represents a city with its details.
type City struct {
	Code         uint      `db:"code" bson:"code" json:"code"`
	Name         string    `db:"name" bson:"name" json:"name"`
	ProvinceCode uint      `db:"province_code" bson:"province_code" json:"province_code"`
	Province     *Province `db:"province" bson:"province" json:"province"`
}

// Cities returns a list of cities belonging to a specific province.
func Cities(province uint) []City {
	var res []City
	for _, city := range cities {
		if city.ProvinceCode == province {
			res = append(res, city)
		}
	}
	return res
}

// FindCity finds a city by its code and populates its province details.
func FindCity(code uint) *City {
	if city, ok := cities[code]; ok {
		if province, ok := provinces[city.ProvinceCode]; ok {
			city.Province = &province
		}
		return &city
	}
	return nil
}

